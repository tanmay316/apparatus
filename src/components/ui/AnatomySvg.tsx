// ─── Canvas rendering helper ──────────────────────────────────────
export function drawAnatomyOnCanvas(
  ctx: CanvasRenderingContext2D,
  view: 'front' | 'back',
  activeMuscles: Set<MuscleRegion>,
  x: number,
  y: number,
  scale: number = 1
) {
  ctx.save();
  ctx.translate(x, y);
  ctx.scale(scale, scale);

  const muscles = view === 'front' ? FRONT_MUSCLES : BACK_MUSCLES;
  const baseD = view === 'front' ? FRONT_BASE_D : BACK_BASE_D;

  // 1. Base connective shapes (head, neck, knees, ...) — always inactive.
  //    No separate hand-drawn silhouette is needed: the base shapes plus every
  //    muscle path below tile together with zero gaps to form the full body.
  ctx.fillStyle = MUSCLE_INACTIVE_FILL;
  ctx.strokeStyle = MUSCLE_INACTIVE_STROKE;
  ctx.lineWidth = 0.6;
  ctx.fill(new Path2D(baseD));
  ctx.stroke(new Path2D(baseD));

  // 2. Inactive muscles first (so active ones can be layered cleanly on top).
  for (const muscle of muscles) {
    if (!activeMuscles.has(muscle.id)) {
      const path = new Path2D(muscle.d);
      ctx.fillStyle = MUSCLE_INACTIVE_FILL;
      ctx.fill(path);
      ctx.strokeStyle = MUSCLE_INACTIVE_STROKE;
      ctx.lineWidth = 0.6;
      ctx.stroke(path);
    }
  }

  // 3. Active muscles (glowing orange), drawn last so the glow isn't clipped
  //    by neighbouring regions.
  for (const muscle of muscles) {
    if (activeMuscles.has(muscle.id)) {
      const path = new Path2D(muscle.d);

      ctx.shadowColor = ACTIVE_ORANGE_GLOW;
      ctx.shadowBlur = 6;
      ctx.fillStyle = ACTIVE_ORANGE;
      ctx.fill(path);

      ctx.shadowBlur = 0;
      ctx.strokeStyle = ACTIVE_ORANGE_STROKE;
      ctx.lineWidth = 0.8;
      ctx.stroke(path);
    }
  }

  ctx.restore();
}

// ─── Optional: pure-SVG React component ───────────────────────────
// Handy for on-screen display (crisp at any size, no canvas redraw loop).
// Use `drawAnatomyOnCanvas` above only where you actually need a raster —
// e.g. compositing the final shareable PNG.
export function AnatomyFigureSVG({
  view,
  activeMuscles,
  className,
}: {
  view: 'front' | 'back';
  activeMuscles: Set<MuscleRegion>;
  className?: string;
}) {
  const muscles = view === 'front' ? FRONT_MUSCLES : BACK_MUSCLES;
  const baseD = view === 'front' ? FRONT_BASE_D : BACK_BASE_D;

  return (
    <svg
      viewBox={`0 0 ${ANATOMY_WIDTH} ${ANATOMY_HEIGHT}`}
      className={className}
      xmlns="http://www.w3.org/2000/svg"
    >
      <path d={baseD} fill={MUSCLE_INACTIVE_FILL} stroke={MUSCLE_INACTIVE_STROKE} strokeWidth={0.6} />
      {muscles.map((muscle) => {
        const active = activeMuscles.has(muscle.id);
        return (
          <path
            key={muscle.id}
            d={muscle.d}
            fill={active ? ACTIVE_ORANGE : MUSCLE_INACTIVE_FILL}
            stroke={active ? ACTIVE_ORANGE_STROKE : MUSCLE_INACTIVE_STROKE}
            strokeWidth={active ? 0.8 : 0.6}
            style={active ? { filter: `drop-shadow(0 0 3px ${ACTIVE_ORANGE_GLOW})` } : undefined}
          />
        );
      })}
    </svg>
  );
}